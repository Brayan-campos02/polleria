    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        dni = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el DNI del repartidor"));
        codigoRepartidor = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el codigo del repartidor"));
        repartidor = polleria.buscarRepartidorCodigo(codigoRepartidor);
        if (repartidor == null) {
            repartidor = polleria.buscarRepartidorCodigo(codigoRepartidor);

            if (repartidor == null) {
                String nombreRepartidor = JOptionPane.showInputDialog("Ingrese el nombre del repartidor");
                String fechaNacimientoRepartidor = JOptionPane.showInputDialog("Ingrese la fecha de nacimiento del repartidor");
                String direccionRepartidor = JOptionPane.showInputDialog("Ingrese la direccion del repartidor");
                int telefonoRepartidor = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el telefono del repartidor"));
                String turnoRepartidor = JOptionPane.showInputDialog("Ingrese el turno del repartidor");

                JOptionPane.showMessageDialog(null, "Se ha contratado al repartidor " + nombreRepartidor);
                repartidor = new Repartidor(dni, nombreRepartidor, fechaNacimientoRepartidor, direccionRepartidor, telefonoRepartidor, codigoRepartidor, turnoRepartidor);
                polleria.contratarRepartidor(repartidor);
                regPersonas.registrarPersona(repartidor);

            } else {
                JOptionPane.showMessageDialog(null, "El repartidor con codigo " + codigoRepartidor + " ya esta registrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "El repartidor con DNI " + dni + " ya esta registrado");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

