    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        dni = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el DNI del mesero"));
        codigoMesero = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el código del mesero"));
        mesero = polleria.buscarMeseroCodigo(codigoMesero);
        if (mesero == null) {
            mesero = polleria.buscarMeseroCodigo(codigoMesero);

            if (mesero == null) {
                String nombreMesero = JOptionPane.showInputDialog("Ingrese el nombre del mesero");
                String fechaNacimientoMesero = JOptionPane.showInputDialog("Ingrese la fecha de nacimiento del mesero");
                String direccionMesero = JOptionPane.showInputDialog("Ingrese la dirección del mesero");
                int telefonoMesero = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el teléfono del mesero"));
                String turnoMesero = JOptionPane.showInputDialog("Ingrese el turno del mesero");

                JOptionPane.showMessageDialog(null, "Se ha contratado al mesero " + nombreMesero);
                mesero = new Mesero(dni, nombreMesero, fechaNacimientoMesero, direccionMesero, telefonoMesero, codigoMesero, turnoMesero);
                polleria.contratarMesero(mesero);
                regPersonas.registrarPersona(mesero);

            } else {
                JOptionPane.showMessageDialog(null, "El mesero con codigo " + codigoMesero + " ya está registrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "El mesero con DNI " + dni + " ya está registrado");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

